let item = prompt()

switch (item) {
    case '1':
        alert('!');
        break;
    case '2':
        alert('@');
        break;
    case '3':
        alert('#');
        break;
    case '4':
        alert('$');
        break;
    case '5':
        alert('%');
        break;
    case '6':
        alert('^');
        break;
    case '7':
        alert('&');
        break;
    case '8':
        alert('*');
        break;
    case '9':
        alert('(');
        break;

    default:
        alert('error')
        break;
}